export function getProperty(id) {
  return properties.find((p) => p.id === id);
}

/**
 * Free-text filter over the property list.
 * Splits the query into whitespace-separated terms and requires every term to
 * appear somewhere in the property's searchable text (AND semantics).
 * An empty / whitespace query returns the full list.
 */
export function filterProperties(query, list = properties) {
  const terms = String(query || "")
    .trim()
    .toLowerCase()
    .split(/\s+/)
    .filter(Boolean);

  if (terms.length === 0) return list;

  return list.filter((p) => {
    const haystack = [
      p.address,
      p.city,
      p.hood,
      p.tag,
      p.rooms,
      p.size,
      p.price,
      p.beds,
      p.baths,
      ...(p.features || []),
    ]
      .join(" ")
      .toLowerCase();

    return terms.every((term) => haystack.includes(term));
  });
}
